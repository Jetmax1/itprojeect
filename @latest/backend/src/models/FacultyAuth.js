import mongoose from "mongoose";

const facultyAuthSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      lowercase: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
    },
  },
  {
    timestamps: true,
    collection: "faculty_logins",
  }
);

export default mongoose.model("FacultyAuth", facultyAuthSchema);
